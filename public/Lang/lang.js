JsLanguage.prototype.getLangArr=function()
{
	var arr;
	if(this.langtype=="zh_cn")
	{	
		arr=this.jsLangArrZh_CN;
	}
	else
	{	
		arr=this.jsLangArrEn;
	}
	return arr;
}



